import cloudinary
import cloudinary.uploader

class UploadFileService:
    """Handles uploading and resizing user avatar images via Cloudinary."""

    def __init__(self, cloud_name, api_key, api_secret):
        self.cloud_name = cloud_name
        self.api_key = api_key
        self.api_secret = api_secret
        cloudinary.config(
            cloud_name=self.cloud_name,
            api_key=self.api_key,
            api_secret=self.api_secret,
            secure=True,
        )

    @staticmethod
    def upload_file(file, username) -> str:
        """
        Upload a file to Cloudinary and return a resized image URL.

        :param file: The uploaded file object.
        :param username: Used to build a unique public ID for the image.
        :return: URL of the uploaded image, cropped to 250x250.
        """
        
        public_id = f"RestApp/{username}"
        r = cloudinary.uploader.upload(file.file, public_id=public_id, overwrite=True)
        src_url = cloudinary.CloudinaryImage(public_id).build_url(
            width=250, height=250, crop="fill", version=r.get("version")
        )
        return src_url
